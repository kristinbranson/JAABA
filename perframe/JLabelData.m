        obj.gt_labels(expi).imp_t1s = {};
        obj.gt_labelstats(expi).nflies_labeled = 0;
        obj.gt_labelstats(expi).nbouts_labeled = 0;
      end
      
      % TODO: update windowdata
      
      success = true;
      
    end
    
