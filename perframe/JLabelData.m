        'moviefilestr',obj.GetFileName('movie'),...
        'perframedir',obj.GetFileName('perframedir'),...
        'default_landmark_params',obj.landmark_params,...
        'perframe_params',obj.perframe_params,...
        'rootwritedir',obj.rootoutputdir);
      
